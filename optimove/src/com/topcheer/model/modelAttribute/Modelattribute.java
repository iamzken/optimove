package com.topcheer.model.modelAttribute;

public class Modelattribute {
		
	private  String  id ;
	private  String  tablename ;
	private  String  modelattribute ;
	private  String  modelattributename ;
	private  String  modelattributetype ;
	private  String  modelattributeflag ;
	private  String  canSearch ;
	private  String  canDisplay ;

	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
    
	public String getTablename() {
		return tablename;
	}
	
	public void setTablename(String tablename) {
		this.tablename = tablename;
	}
	
    
	public String getModelattribute() {
		return modelattribute;
	}
	
	public void setModelattribute(String modelattribute) {
		this.modelattribute = modelattribute;
	}
	
    
	public String getModelattributename() {
		return modelattributename;
	}
	
	public void setModelattributename(String modelattributename) {
		this.modelattributename = modelattributename;
	}
	
	public String getModelattributetype() {
		return modelattributetype;
	}
	
	public void setModelattributetype(String modelattributetype) {
		this.modelattributetype = modelattributetype;
	}
	
    
	public String getModelattributeflag() {
		return modelattributeflag;
	}
	
	public void setModelattributeflag(String modelattributeflag) {
		this.modelattributeflag = modelattributeflag;
	}

	public String getCanSearch() {
		return canSearch;
	}

	public void setCanSearch(String canSearch) {
		this.canSearch = canSearch;
	}

	public String getCanDisplay() {
		return canDisplay;
	}

	public void setCanDisplay(String canDisplay) {
		this.canDisplay = canDisplay;
	}
	
}
